# Comparar dos Variables son iguales mediante el '=='
# Si una Variable es menor a una '<'
# Si una Variable es mayor que la otra '>'
# Si son distintas '!='
# Si esta es menor o igual a otra o mayor o igual '<=' y '>='

if 2 == 2:
    print('2 es igual a 2')

if 2 == 3:
    print('2 es igual a 3')

if 2 > 5:
    print('2 es mayor a 5')

if 5 > 2:
    print('5 es mayor a 2')

if 2 != 2:
    print('2 es distinto a 2')

if 3 != 2:
    print('3 es distinto a 2')

if 3 >= 2:
    print('3 es mayor o igual a 2')

if 3 <= 3:
    print('3 es menor o igual a 3')
