# Creamos una nueva lista (Puede ser Numerica o de Caracteres)
Mueble = ['Mesa', 'Repisa', 'Velador', 'Estante']
print(Mueble)

# Elemento '.pop()' es utilizado para eliminar el ultimo elemento de la lista
Mueble.pop()
print(Mueble)

# Agregemos el Estante nuevamente
Mueble.append('Estante')
print(Mueble)

# Para Eliminar un Elemento en Particular usaremos el '.remove()' ahora sacaremos "Repisa"
Mueble.remove('Repisa')
print(Mueble)

# Agregemos la Repisa nuevamente
Mueble.append('Repisa')
print(Mueble)